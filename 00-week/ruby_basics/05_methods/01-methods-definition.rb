# METHODS: definition
#
# "What are Ruby methods?" OR "Does Ruby have functions?"
# Ruby methods are very similar to functions in other programming languages like C or Perl 
# Ruby methods are used to bundle one or more repeatable statements into a single unit.
# Method names should begin with a lowercase letter.



def writeln(string)
  p(string)
end

writeln("Hello, World!") # => "Hello, World!"

# >> "Hello, World!"
